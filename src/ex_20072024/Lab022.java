package ex_20072024;

public class Lab022 {
     public static void main(String[] args) {
        //Formatting
        int age=32;
         System.out.println("your age is:"+age);
         System.out.printf("My age is >> %d",age);

         float fl = 5.6f;
         System.out.printf("The floating value is >> %f",fl);
         String str = "Rudra dathaprasad";
         System.out.printf("my name is %s", str);
         System.out.println();

         char ch = 'D';
         System.out.printf("My name will start with >>  %c",ch);

//                %d -->any integer byte, short, int,or long
//                %s --> string
//                %c --> Char
//                %F --> float







    }
}
